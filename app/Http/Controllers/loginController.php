    public function action(Request $request)
    {
        $data = [
            'email' => $request->input('email'),
            'password' => $request->input('password'),
        ];

        if (Auth::Attempt($data)) {
            return redirect('/welcome');
        }else{
            Session::flash('error', 'Email atau Password Salah');
            return redirect('/');
        }
    }
